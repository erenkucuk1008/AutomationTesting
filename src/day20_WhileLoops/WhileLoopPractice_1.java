package day20_WhileLoops;

public class WhileLoopPractice_1 {

    public static void main(String[] args) {

        int num= 1;
        while (num<=10){
            System.out.print(num+" "); // 1 ---- 10
            num++;
        }

        System.out.println();

        int num1 = 1;
        while (num1<=10){
            num1++;
            System.out.print(num1+" ");  // 2 ---- 11
        }
    }
}
