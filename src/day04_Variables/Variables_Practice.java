package day04_Variables;

public class Variables_Practice {

    public static void main(String[] args) {

        byte num1 = 100;
        short num2 = 10000;
        double num3 = 3.5;
        float num4 = 4.5f;
        long num5 = 999999999999L;
        int num6 = 85000;

        System.out.println("Num1: "+num1+", Num2: "+num2+", Num3: "+num3+", Num4: "+num4+", Num5: "+num5+", Num6: "+num6);
    }
}
