package day03_Variables;

public class EscapeSequences {

    public static void main(String[] args) {

    // \n:
        System.out.println("Hello Everyone");
        System.out.println("My name is ErenK");

        System.out.println("==================================");

        System.out.println("Hello Everyone\nMy name is ErenK");

        System.out.println("==================================");

    // \t:
        System.out.println("\tToday I learned Java");
        System.out.println("it was so much fun");

        System.out.println("==================================");

    // \\: \
        System.out.println("/");  //prints single front slash
        System.out.println("\\"); //prints single backslash

        System.out.println("==================================");

        System.out.println("       ^");
        System.out.println("      / \\");
        System.out.println("     /   \\");
        System.out.println("    /     \\");
        System.out.println("   /       \\");
        System.out.println("   ---------");

    // ' or \'
        System.out.println("My name is 'Eren'");
        System.out.println("My name is \'Eren\'");

        System.out.println("==================================");

    // \"
        System.out.println("My name is \"Eren\"");

        System.out.println("My \"favorite\" movie \"is\": \"INEK SABAN\"");



    }

}
